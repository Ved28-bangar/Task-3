# Taking input from the user and converting it to an integer
num1 = int(input("Enter the first number: "))  

# Taking another input and converting it to an integer
num2 = int(input("Enter the second number: "))  

# Performing addition and printing the result
Addition = num1 + num2  
print("Addition =", Addition)  

# Performing subtraction and printing the result
subtraction = num1 - num2  
print("subtraction =", subtraction)  

# Performing multiplication and printing the result
multiplication = num1 * num2  
print("multiplication =", multiplication)  

# Performing division and printing the result (note: float division)
Division = num1 / num2  
print("Division =", Division)